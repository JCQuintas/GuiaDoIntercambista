public class Friend {

    public int image;
    public String name;
    public String country;
    public String city;
    public int distance;

    public Friend(int image, String name, String country, String city, int distance) {
        super();
        this.image = image;
        this.name = name;
        this.country = country;
        this.city = city;
        this.distance = distance;
    }
}
